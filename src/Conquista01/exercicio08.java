//Criar um código que declara e inicializa variáveis
// primitivas e atribui o seu próprio valor somado a ele
// mesmo utilizando operadores de atribuição direta (+=, -=, *=...)

public class exercicio08 {
    public static void main(String[] args) {
        int numero01=200;
        numero01+=30;
        numero01-=50;
        numero01*=2;
        numero01/=10;
        numero01%=8;
        System.out.println(numero01);
    }
}
